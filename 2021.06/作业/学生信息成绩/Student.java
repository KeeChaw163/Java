  package chengji;

  public class Student {
	  String name;
	  String sex;
	  //定义全局变量	 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public void show(){
		System.out.println("信息："+this.getName()+this.getSex());
	}

  }    
	     
 
		    
		    
		    
		    
		    
		 

